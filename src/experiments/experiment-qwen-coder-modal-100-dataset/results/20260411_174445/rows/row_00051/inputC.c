#include <stdio.h>
#include <ctype.h>

int rot[24][7] = {
	{ 0,1,2,3,4,5,6 },{ 0,1,3,5,2,4,6 },{ 0,1,4,2,5,3,6 },{ 0,1,5,4,3,2,6 },
	{ 0,2,6,3,4,1,5 },{ 0,2,3,1,6,4,5 },{ 0,2,1,4,3,6,5 },{ 0,2,4,6,1,3,5 },
	{ 0,3,1,2,5,6,4 },{ 0,3,2,6,1,5,4 },{ 0,3,5,1,6,2,4 },{ 0,3,6,5,2,1,4 },
	{ 0,4,1,5,2,6,3 },{ 0,4,2,1,6,5,3 },{ 0,4,5,6,1,2,3 },{ 0,4,6,2,5,1,3 },
	{ 0,5,1,3,4,6,2 },{ 0,5,3,6,1,4,2 },{ 0,5,4,1,6,3,2 },{ 0,5,6,4,3,1,2 },
	{ 0,6,2,4,3,5,1 },{ 0,6,3,2,5,4,1 },{ 0,6,5,3,4,2,1 },{ 0,6,4,5,2,3,1 } };

int con[9][4][3] = { {{0},{0},{0},{0} }, { {0},{0},{0},{0} }, { {1,4,3},{0},{0},{0} },
	{ {2,2,5},{0},{0},{0} }, { {1,2,5},{3,3,4},{0},{0} }, { {1,6,1},{0},{0},{0} },
	{ {2,6,1},{5,4,3},{0},{0} }, { {3,6,1},{6,2,5},{0},{0} }, { {4,6,1},{5,2,5},{7,3,4},{0} } };

char dice[9][8];
char kari[9][8], f[9];

int check(char c1, char c2)
{
	if (c1 <= 'Z' && c2 <= 'Z') return 0;
	if (c1 >= 'a' && c2 >= 'a') return 0;
	return toupper(c1) == toupper(c2);
}

int combi(int n)
{
	int i, j, k, n0, p, p0;
	if (n == 9) return 1;
	for (i = 1; i <= 8; i++) {
		if (f[i]) continue;
		for (j = 0; j < 24; j++) {
			for (k = 1; k <= 6; k++) kari[n][k] = dice[i][rot[j][k]];
			for (k = 0; con[n][k][0]; k++) {
				n0 = con[n][k][0], p = con[n][k][1], p0 = con[n][k][2];
				if (!check(kari[n0][p], kari[n][p0])) goto Next;
			}
			f[i] = 1;
			if (combi(n + 1)) return 1;
			f[i] = 0;
		Next:;
		}
	}
	return 0;
}

int main()
{
	int i;

	while (1) {
		for (i = 1; i <= 8; i++) {
			scanf("%s", dice[i] + 1);
			if (i == 1 && dice[i][1] == '0') return 0;
			f[i] = 0;
		}
		puts(combi(1) ? "YES" : "NO");
	}
	return 0;
}